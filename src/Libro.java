
public class Libro {
    String titulo, nombreA, apellidoA, editorial, isbn, fecha;
    int precio;

    public Libro(String titulo, String nombreA, String apellidoA, String editorial, String isbn, String fecha, int precio){
        this.titulo = titulo;
        this.nombreA = nombreA;
        this.apellidoA = apellidoA;
        this.editorial = editorial;
        this.isbn = isbn;
        this.fecha = fecha;
        this.precio = precio;
    }
    
}
